CREATE TABLE VACCINATION_RECORD (
    AADHAR_NUMBER NUMBER,
    WORKER_ID NUMBER,
    CENTER_ID VARCHAR(10),
    DATETIME TIMESTAMP,
    REFERENCE_ID NUMBER PRIMARY KEY,
    AGE NUMBER,
    VACCINE_ID NUMBER,
    FOREIGN KEY (AADHAR_NUMBER) REFERENCES CITIZENS(AADHAR_NUMBER),
    FOREIGN KEY (WORKER_ID) REFERENCES HEALTH_WORKERS(WORKER_ID),
    FOREIGN KEY (CENTER_ID) REFERENCES VACCINATION_CENTER(CENTER_ID),
    FOREIGN KEY (VACCINE_ID) REFERENCES VACCINE(VACCINE_ID)
);

