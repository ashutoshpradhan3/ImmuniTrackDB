    WORKER_ID NUMBER PRIMARY KEY,
    POST VARCHAR(30),
    HOSPITAL_ID NUMBER,
    AADHAR_NUMBER NUMBER,
    FOREIGN KEY (HOSPITAL_ID) REFERENCES HOSPITAL(HOSPITAL_ID),
    FOREIGN KEY (AADHAR_NUMBER) REFERENCES CITIZENS(AADHAR_NUMBER)
